function createVideoElement(lesson) {
    const videoLesson = document.createElement('div');
    videoLesson.className = 'video-lesson';

    const titleContainer = document.createElement('div');
    titleContainer.className = 'video-title-container flex items-center justify-between';

    const title = document.createElement('h3');
    title.textContent = lesson.name;
    title.className = 'text-lg font-bold';
    titleContainer.appendChild(title);

    // Checkbox for selecting videos
    const checkbox = document.createElement('input');
    checkbox.type = 'checkbox';
    checkbox.className = 'video-checkbox ml-4';
    checkbox.checked = JSON.parse(localStorage.getItem(lesson.id)) || false;

    // Store selection in localStorage
    checkbox.addEventListener('change', () => {
        localStorage.setItem(lesson.id, checkbox.checked);
    });

    titleContainer.appendChild(checkbox);
    videoLesson.appendChild(titleContainer);

    const videoContainer = document.createElement('div');
    videoContainer.className = 'video-container relative w-full pt-[56.25%]'; // 16:9 aspect ratio

    // Create a placeholder div instead of iframe
    const placeholder = document.createElement('div');
    placeholder.className = 'video-placeholder absolute top-0 left-0 w-full h-full border-0 bg-gray-200 flex items-center justify-center';
    placeholder.textContent = 'Loading...';

    // Store the video data as attributes
    placeholder.dataset.fileId = lesson.id;
    placeholder.dataset.lessonName = lesson.name;

    videoContainer.appendChild(placeholder);
    videoLesson.appendChild(videoContainer);

    return videoLesson;
}

function loadVideo(placeholder) {
    const fileId = placeholder.dataset.fileId;
    const lessonName = placeholder.dataset.lessonName;

    const iframe = document.createElement('iframe');
    iframe.src = `https://drive.google.com/file/d/${fileId}/preview`;
    iframe.className = 'absolute top-0 left-0 w-full h-full border-0';
    iframe.allow = 'autoplay; encrypted-media';
    iframe.allowFullscreen = true;
    iframe.title = lessonName;

    placeholder.parentNode.replaceChild(iframe, placeholder);
}


function setupLazyLoading() {
    const observer = new IntersectionObserver((entries, observer) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                loadVideo(entry.target);
                observer.unobserve(entry.target);
            }
        });
    }, {
        root: null,
        rootMargin: '0px',
        threshold: 0.1
    });

    document.querySelectorAll('.video-placeholder').forEach(placeholder => {
        observer.observe(placeholder);
    });
}

async function createHtmlElement(file) {
    const htmlElement = document.createElement('div');
    htmlElement.className = 'html-file';

    const title = document.createElement('h3');
    title.textContent = file.name;
    htmlElement.appendChild(title);

    const contentContainer = document.createElement('div');
    contentContainer.className = 'html-content border p-4 rounded bg-gray-100';
    contentContainer.textContent = 'Loading HTML content...'; // Placeholder text while loading
    htmlElement.appendChild(contentContainer);

    try {
        // Fetch the raw HTML content from the Drive file
        const downloadUrl = `https://drive.google.com/uc?export=download&id=${file.id}`;
        const response = await fetch(downloadUrl);

        if (response.ok) {
            const htmlText = await response.text();
            contentContainer.innerHTML = htmlText; // Render the fetched HTML content
        } else {
            contentContainer.textContent = `Failed to load content: ${response.status}`;
        }
    } catch (error) {
        contentContainer.textContent = `Error loading content: ${error.message}`;
    }

    return htmlElement;
}


